let notAluno = Number(prompt("Digite a nota:"));
if (notAluno >= 90) {
    alert("Conceito A");
} else if (notAluno >= 80) {
    alert("Conceito B");
} else if (notAluno >= 70) {
    alert("Conceito C");
} else if (notAluno >= 60) {
    alert("Conceito D");
} else {
    alert("Conceito F");
}